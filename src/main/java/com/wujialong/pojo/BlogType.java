package com.wujialong.pojo;

public class BlogType {
	private int id;
	private String type;
	private int blog_ID;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getBlog_ID() {
		return blog_ID;
	}
	public void setBlog_ID(int blog_ID) {
		this.blog_ID = blog_ID;
	}
	
}
